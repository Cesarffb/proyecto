    private void b_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_buscarActionPerformed
        // Busqueda rapida de actualizacion
        String ID = t_busqueda.getText();
        if(ID.equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese Nombre o Cedula");
        }
        //Session st = HibernateUtil.getSessionFactory().openSession();
        //st.beginTransaction();
        List<Funcionario> lista = (List<Funcionario>)st.createQuery("From Funcionario").list();

        try{
            int id = Integer.parseInt(ID);
            Funcionario pro = (Funcionario)st.load(Funcionario.class, id
            );
            EnviarRes(pro.getCedula(), pro.getNombre(), pro.getApellido(),
                pro.getTelefono(), pro.getCorreo(), pro.getPass(), pro.getCargo());
        }
        catch(Exception e){
            //Nombre ingresado
            System.out.println("Se ha ingresado un nombre de producto");
            for (Iterator<Funcionario> it = lista.iterator(); it.hasNext();) {
                Funcionario pro = it.next();
                if(pro.getCedula().equals(ID)){
                    System.out.println(pro.getCedula());
                    EnviarRes(pro.getCedula(), pro.getNombre(), pro.getApellido(),
                                pro.getTelefono(), pro.getCorreo(), pro.getPass(), pro.getCargo());
                    t_busqueda.requestFocus();
                    panel_busqueda.setVisible(false);
                    return;
                }
            }
            JOptionPane.showMessageDialog(this, "No encontrado");
            return;
        }
        t_busqueda.requestFocus();
        panel_busqueda.setVisible(false);
        //st.close();
    }//GEN-LAST:event_b_buscarActionPerformed

