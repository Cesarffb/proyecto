    void LlenarTabla(List<Funcionario> listaF) {
        DefaultTableModel dtm = (DefaultTableModel) this.tabla.getModel();
        //System.out.println(ListaProductos.size());
        //Object[] data = new Object[5];
        for (Iterator<Funcionario> it = listaF.iterator(); it.hasNext();) {
            Funcionario pro = it.next();
            dtm.addRow(new Object[]{
                            pro.getCedula(), pro.getNombre(), pro.getApellido(),
                pro.getCorreo(), pro.getPass(), pro.getCargo()
            });
        }
        tabla.setModel(dtm);
    }
