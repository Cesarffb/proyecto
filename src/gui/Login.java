    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Inicio de Sesion 
        String ci, Pass = "";
        ci = t_ci.getText();
        char[] pass = t_pass.getPassword();
        for (int i = 0; i < pass.length; i++) {
            Pass += pass[i];
        }
        List<Funcionario> lista = (List<Funcionario>)st.createQuery("From Funcionario").list();
        for (Iterator<Funcionario> it = lista.iterator(); it.hasNext();) {
            Funcionario pro = it.next();
            String contra = pro.getPass();
            System.out.println(contra);
            System.out.println(Pass);
            System.out.println(pro.getCedula());
            System.out.println(ci);
            if(pro.getCedula().equals(ci)){
                // ci correcto
                if(contra.equals(Pass)){
                    // pass correcto
                    if(pro.getCargo().equals(0)){
                        //Modo admin
                        menu ui = new menu();
                        this.dispose();
                        ui.enviarUser(pro);
                        ui.setVisible(true);
                        return;
                    }
                    else{
                        //Modo normal
                        menu ui = new menu(false);
                        ui.enviarUser(pro);
                        this.dispose();
                        ui.setVisible(true);
                        return;
                    }
                }
            }
        }
        JOptionPane.showMessageDialog(this, "Datos de acceso incorrectos", "Error de acceso", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed

